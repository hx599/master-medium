def set_para(para, text):
    for run in para.runs:
        run.text = ''
    if para.runs:
        run = para.runs[0]
        run.text = text
        run.font.name = '宋体'
        run.element.rPr.rFonts.set(qn('w:eastAsia'), '宋体')
        run.font.size = Pt(12)
    else:
        run = para.add_run(text)
        run.font.name = '宋体'
        run.element.rPr.rFonts.set(qn('w:eastAsia'), '宋体')
        run.font.size = Pt(12)

