def _set_run_font(run, font_name='宋体', size_pt=12, bold=False):
    run.font.name = font_name
    run.element.rPr.rFonts.set(qn('w:eastAsia'), font_name)
    run.font.size = Pt(size_pt)
    run.bold = bold
    run.font.color.rgb = RGBColor(0, 0, 0)


def _make_paragraph_block(doc, item):
    level = item[0]

    para = doc.add_paragraph()

    if level == 'h1':
        text = item[1]
        para.paragraph_format.space_before = Pt(12)
        para.paragraph_format.space_after = Pt(6)
        para.paragraph_format.first_line_indent = Cm(0)
        run = para.add_run(text)
        _set_run_font(run, font_name='黑体', size_pt=16, bold=True)

    elif level == 'h2':
        text = item[1]
        para.paragraph_format.space_before = Pt(8)
        para.paragraph_format.space_after = Pt(4)
        para.paragraph_format.first_line_indent = Cm(0)
        run = para.add_run(text)
        _set_run_font(run, font_name='黑体', size_pt=15, bold=True)

    elif level == 'h3':
        text = item[1]
        para.paragraph_format.space_before = Pt(6)
        para.paragraph_format.space_after = Pt(2)
        para.paragraph_format.first_line_indent = Cm(0)
        run = para.add_run(text)
        _set_run_font(run, font_name='黑体', size_pt=14, bold=True)

    elif level == 'h4':
        text = item[1]
        para.paragraph_format.space_before = Pt(4)
        para.paragraph_format.space_after = Pt(2)
        para.paragraph_format.first_line_indent = Cm(0)
        run = para.add_run(text)
        _set_run_font(run, font_name='黑体', size_pt=13, bold=True)

    elif level == 'p':
        text = item[1]
        para.paragraph_format.first_line_indent = Cm(0.74)
        para.paragraph_format.line_spacing = 1.5
        para.paragraph_format.space_after = Pt(0)
        run = para.add_run(text)
        _set_run_font(run, font_name='宋体', size_pt=12, bold=False)

    elif level == 'fig':
        text = item[1]
        para.alignment = WD_ALIGN_PARAGRAPH.CENTER
        para.paragraph_format.first_line_indent = Cm(0)
        para.paragraph_format.space_before = Pt(6)
        para.paragraph_format.space_after = Pt(0)
        run = para.add_run(text)
        _set_run_font(run, font_name='宋体', size_pt=12, bold=False)

    elif level == 'cap':
        text = item[1]
        para.alignment = WD_ALIGN_PARAGRAPH.CENTER
        para.paragraph_format.first_line_indent = Cm(0)
        para.paragraph_format.space_after = Pt(6)
        run = para.add_run(text)
        _set_run_font(run, font_name='宋体', size_pt=10.5, bold=False)

    elif level == 'eq':
        body, num = item[1], item[2]
        para.alignment = WD_ALIGN_PARAGRAPH.CENTER
        para.paragraph_format.first_line_indent = Cm(0)
        para.paragraph_format.space_before = Pt(2)
        para.paragraph_format.space_after = Pt(2)
        run = para.add_run(f'{body}                                  {num}')
        _set_run_font(run, font_name='Times New Roman', size_pt=12, bold=False)
        run.element.rPr.rFonts.set(qn('w:eastAsia'), '宋体')

    else:
        raise ValueError(f'Unknown level: {level}')

    return para._element


def _make_table_block(doc, item):
    headers = item[1]
    rows = item[2]
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    table.style = 'Table Grid'

    # 表头
    for j, header in enumerate(headers):
        cell = table.rows[0].cells[j]
        cell.text = ''
        p = cell.paragraphs[0]
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run = p.add_run(header)
        _set_run_font(run, font_name='宋体', size_pt=10, bold=True)

    # 表体
    for i, row_data in enumerate(rows):
        for j, val in enumerate(row_data):
            cell = table.rows[i + 1].cells[j]
            cell.text = ''
            p = cell.paragraphs[0]
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            run = p.add_run(str(val))
            _set_run_font(run, font_name='宋体', size_pt=10, bold=False)

    return table._element


def _find_anchor(doc, prefix):
    for para in doc.paragraphs:
        if para.text.startswith(prefix):
            return para
    raise SystemExit(f'未找到 anchor: {prefix!r}')


def main():
    doc = Document(str(SRC_DOCX))
    s4_last = _find_anchor(doc, ANCHOR_AFTER_S4)
    insert_after = s4_last._element

    for item in BLOCKS:
        level = item[0]
        if level == 'table':
            new_elem = _make_table_block(doc, item)
        else:
            new_elem = _make_paragraph_block(doc, item)
        new_elem.getparent().remove(new_elem)
        insert_after.addnext(new_elem)
        insert_after = new_elem

    doc.save(str(DST_DOCX))
    print(f'§5.1 / §5.1.4 inserted ({len(BLOCKS)} blocks) into {DST_DOCX}')


if __name__ == '__main__':
    main()
