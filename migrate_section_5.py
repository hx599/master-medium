BLOCKS.append(('p', '平均精度（Average Accuracy，AA）：'))
BLOCKS.append(('eq', 'AA = (1 / C) ∑_{i=1}^C TP_i / (TP_i + FN_i)', '(5-22)'))
BLOCKS.append(('p', '其中，TP_i表示类别i的真正例数，FN_i表示类别i的假负例数。'))
BLOCKS.append(('p', 'Kappa系数（Kappa）：'))
BLOCKS.append(('eq', 'Kappa = (OA − PA) / (1 − PA)', '(5-23)'))
BLOCKS.append(('p', '其中，PA是按类别分布计算的预期总体准确率，计算公式为：'))
BLOCKS.append(('eq', 'PA = (1 / N²) ∑_{i=1}^C [ (∑_{j=1}^C TP_ij) / N ] · [ (∑_{j=1}^C TP_ji) / N ]', '(5-24)'))
BLOCKS.append(('p', '其中，TP_ij是混淆矩阵中类别i和j的交集。'))

