def find_paragraph_by_text(doc, search_text):
    """在文档中查找包含指定文本的段落，返回段落对象。"""
    for para in doc.paragraphs:
        if search_text in para.text:
            return para
    return None


def apply_replacements(doc, replacements):
    """应用所有文本替换规则。"""
    modified_count = 0
    for i, rule in enumerate(replacements):
        para = find_paragraph_by_text(doc, rule['old'])
        if para is not None:
            old_text = para.text
            new_text = old_text.replace(rule['old'], rule['new'])
            # 清除原有 runs
            for run in para.runs:
                run.text = ''
            # 设置新文本
            if para.runs:
                run = para.runs[0]
                run.text = new_text
                run.font.name = '宋体'
                run.element.rPr.rFonts.set(qn('w:eastAsia'), '宋体')
                run.font.size = Pt(12)
            else:
                run = para.add_run(new_text)
                run.font.name = '宋体'
                run.element.rPr.rFonts.set(qn('w:eastAsia'), '宋体')
                run.font.size = Pt(12)
            modified_count += 1
            print(f'  [OK] 替换 #{i+1}: {rule["old"][:60]}...')
        else:
            print(f'  [WARN] 未找到 #{i+1}: {rule["old"][:60]}...')
    return modified_count


