def set_paragraph_text(para, text, font_name='宋体', font_size=12, bold=False):
    """清空段落并设置新文字，保留原有格式基础。"""
    for run in para.runs:
        run.text = ''
    if para.runs:
        # 保留第一个 run 的设置
        run = para.runs[0]
        run.text = text
        run.font.name = font_name
        run.element.rPr.rFonts.set(qn('w:eastAsia'), font_name)
        run.font.size = Pt(font_size)
        run.bold = bold
    else:
        run = para.add_run(text)
        run.font.name = font_name
        run.element.rPr.rFonts.set(qn('w:eastAsia'), font_name)
        run.font.size = Pt(font_size)
        run.bold = bold


