def _set_run_font(run, font_name='宋体', size_pt=12, bold=False, italic=False):
    run.font.name = font_name
    run.element.rPr.rFonts.set(qn('w:eastAsia'), font_name)
    run.font.size = Pt(size_pt)
    run.bold = bold
    run.italic = italic
    run.font.color.rgb = RGBColor(0, 0, 0)


def _make_para(doc, level, text):
    para = doc.add_paragraph()
    run = para.add_run(text)

    if level == 'h2':
        para.alignment = WD_ALIGN_PARAGRAPH.LEFT
        para.paragraph_format.space_before = Pt(12)
        para.paragraph_format.space_after = Pt(6)
        para.paragraph_format.first_line_indent = Cm(0)
        _set_run_font(run, font_name='黑体', size_pt=15, bold=True)
    elif level == 'h3':
        para.paragraph_format.space_before = Pt(8)
        para.paragraph_format.space_after = Pt(4)
        para.paragraph_format.first_line_indent = Cm(0)
        _set_run_font(run, font_name='黑体', size_pt=14, bold=True)
    elif level == 'h4':
        para.paragraph_format.space_before = Pt(6)
        para.paragraph_format.space_after = Pt(2)
        para.paragraph_format.first_line_indent = Cm(0)
        _set_run_font(run, font_name='黑体', size_pt=13, bold=True)
    elif level == 'eq':
        para.alignment = WD_ALIGN_PARAGRAPH.CENTER
        para.paragraph_format.space_before = Pt(4)
        para.paragraph_format.space_after = Pt(4)
        para.paragraph_format.first_line_indent = Cm(0)
        _set_run_font(run, font_name='Times New Roman', size_pt=11, bold=False, italic=False)
        run.element.rPr.rFonts.set(qn('w:eastAsia'), '宋体')
    elif level == 'fig':
        para.alignment = WD_ALIGN_PARAGRAPH.CENTER
        para.paragraph_format.space_before = Pt(6)
        para.paragraph_format.space_after = Pt(2)
        para.paragraph_format.first_line_indent = Cm(0)
        _set_run_font(run, font_name='宋体', size_pt=11, bold=False, italic=True)
    elif level == 'caption':
        para.alignment = WD_ALIGN_PARAGRAPH.CENTER
        para.paragraph_format.space_before = Pt(2)
        para.paragraph_format.space_after = Pt(8)
        para.paragraph_format.first_line_indent = Cm(0)
        _set_run_font(run, font_name='宋体', size_pt=11, bold=True)
    else:  # 'p'
        para.paragraph_format.first_line_indent = Cm(0.74)
        para.paragraph_format.line_spacing = 1.5
        para.paragraph_format.space_after = Pt(0)
        _set_run_font(run, font_name='宋体', size_pt=12, bold=False)

    return para._element


def main():
    doc = Document(str(SRC_DOCX))

    # 找到 P101"表1显示了..."段，作为锚点
    target_idx = None
    for i, p in enumerate(doc.paragraphs):
        if p.text.strip().startswith('表1显示了两个未知类别情况下的各方法平均分类结果'):
            target_idx = i
            break

    if target_idx is None:
        raise RuntimeError('未找到目标插入位置：以"表1显示了两个未知类别情况下"开头的段落')

    print(f'锚点段落: P{target_idx}: {doc.paragraphs[target_idx].text[:60]}...')
    anchor = doc.paragraphs[target_idx]._element

    # 在文档末尾追加新段落，再 detach 后插到锚点之前
    new_elements = []
    for level, text in SECTIONS:
        new_elem = _make_para(doc, level, text)
        new_elements.append(new_elem)

    for elem in new_elements:
        elem.getparent().remove(elem)
        anchor.addprevious(elem)

    doc.save(str(DST_DOCX))
    print(f'已写入 {len(SECTIONS)} 段到 {DST_DOCX}')


if __name__ == '__main__':
    main()
